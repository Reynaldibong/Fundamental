//find diameter
let r = 7;
let pi = 3.14;
let diameter = 2 * r;
console.log(diameter);

//find circumference
let circumferenceofcircle = pi * diameter;
console.log(circumferenceofcircle.toFixed());

//find area
let areaofcircle = pi * r ** 2;
console.log(areaofcircle.toFixed());
